/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 * Return: sum of two numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: first number
 * @b: second number
 * Return: difference of two numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 * Return: product of two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: first number
 * @b: second number
 * Return: quotient of two numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulo two numbers
 * @a: first number
 * @b: second number
 * Return: remainder of two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
